package Ch02_VariableDataType;

public class _09_variableLevel {
    public static void main(String[] args) {
        int level;       // 변수 선언
        level = 10;      // 변수 값 대입
        System.out.println(level);  // 변수 값 출력

        level = 12;      // 변수 값 변경
        System.out.println(level);  // 변수 값 출력
    }
}
